class lock:
    def __init__(self, path):
        self.path = path
        self.ok = False

    def __enter__(self):
        try:
            fp = os.open(self.path, os.O_CREAT | os.O_EXCL | os.O_WRONLY)
            os.close(fp)
            self.ok = True
        except:
            self.ok = False
        return self.ok

    def __exit__(self, exc_type, exc_val, exc_tb):
        if self.ok:
            try:
                os.unlink(self.path)
            except OSError:
                pass


